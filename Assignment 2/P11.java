class P11
{
	public static void main(String args[])
	{
	for(int i=10;i>0;i=i=i-2)
	{
		for(int j=1;j<(i-2);j=j+2)
		{	System.out.print(" ");
		}
		for(int j=10;j>=i;j--)
		{
			
			System.out.print("*");
		}
		System.out.println("");
	}
	}
}


